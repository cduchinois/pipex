#include "../../inc/libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*new;

	new = (t_list *)malloc(sizeof(t_list *) * 1);
	if (!new)
		return (0);
	new->content = content;
	new->next = NULL;
	return (new);
}
